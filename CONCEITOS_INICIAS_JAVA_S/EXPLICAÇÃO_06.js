// Operadores Logicos

let nome1 = 'Marta'
let nome2 = 'Marta'

let idade1 = 10
let idade2 = '10'

console.log ('= COMPARAÇÂO =')

console.log('= Comparando conteúdo =')
console.log(nome1 == nome2)

console.log('\n= Comparando tipo e conteúdo =')
console.log(nome1 === nome2)

console.log('\n= DIFERENTE =')
console.log('= Comparando conteúdo =')
console.log(idade1 != idade2)

console.log('\n= Comparando tipo e conteúdo =')
console.log(idade1 !== idade2)

console.log('\n= AND =')
console.log(1 < 2 && 1 < 3)

console.log('\n= OR =')
console.log(10 < 2 || 1 < 3)